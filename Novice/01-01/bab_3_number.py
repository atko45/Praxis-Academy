#NUMBER

#Penggunaan Penjumlahan (+) , Pengurangan (-) , Perkalian (*) dan Pembagian (/)
print(2 + 2)  #Penjumlahan
print(50 - 5*6) #Pengurangan  
print((50 - 5*6) / 4) #Kombinasi Pengurangan, Kurung, perkalian dan Pembagian
print(8 / 5) #Pembagian

print('')

#Penggunaan /, // dan %
print(17 / 3) #Sebagai Pembagi bilangan hingga pecahan
print(17 // 3) #Sebagai Pembulat hasil pembagian atau bilangan agar tidak menjadi pecahan
print(17 % 3) #Memperlihatkan sisa dari pembagian 
print(5 * 3 + 2) #Penjumlahan dari hasil penggunaan /, // dan %

print('')

#Penggunaan ** sebagai perpangkatan 
print(5 ** 2) #Memiliki fungsi sama seperti '5' PANGKAT '2'
print(2 ** 7) #Memiliki fungsi sama seperti '2' PANGKAT '7'

print('')

#Penggunaan Sama Dengan (=) 
width = 20 #Penggunaan sama dengan (=) sebagai 'penamaan pada variable/devinisi'
height = 5 * 9 #Penggunaan sama dengan (=) sebagai 'penamaan pada variable/devinisi' 

print(width * height) #Penggabungan dari Kedua Variable yang telah dibuat

print('')

#Penggunaan tanda titik/point (.)

print(4 * 3.75 - 1) #tanda titik/point digunakan sebagai KOMA

print('')

