# OPERATORS
	#	Operators COMPARISON	('Perhatikan Pernyataannya . . . ! ! !')

x = 5
y = 3

print(x == y) # Code ini 'Menyatakan bahwa (x Memiliki nilai yang sama dengan y)' Hasilnya FALSE

print(x != y) # Code ini 'Menyatakan bahwa (x Tidak Memiliki nilai yang sama dengan y)' Hasilnya TRUE

print(x > y) # Code ini 'Menyatakan bahwa (x memiliki nilai yang sama dengan y)' Hasilnya TRUE

print(x < y) # Code ini 'Menyatakan bahwa (x memiliki nilai yang sama dengan y)' Hasilnya FALSE

print(x >= y) # Code ini 'Menyatakan bahwa (x memiliki nilai yang sama dengan y)' Hasilnya TRUE

print(x <= y) # Code ini 'Menyatakan bahwa (x memiliki nilai yang sama dengan y)' Hasilnya FALSE
