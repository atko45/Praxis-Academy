x = 5

print(x > 3 and x < 10) # Code ini 'Menyatakan bahwa (x Lebih dari 3 AND x Kurang dari 10 )' Hasilnya TRUE

print(x > 3 or x > 10) # Code ini 'Menyatakan bahwa (x Lebih dari 3 OR x Lebih dari 10)' Hasilnya TRUE

print(not(x > 3 and x < 10)) # Code ini 'Menyatakan bahwa (x Lebih dari 3 AND x Kurang dari 10)' Hasilnya FALSE
