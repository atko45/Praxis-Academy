x = 100
y = 20

print(x + y) #	Penjumlahan
print(x - y) #	Pengurangan
print(x * y) #	Perkalian
print(x / y) #	Pembagian
print(x % y) #	Pembagian untuk menampilkan 'Pecahan' dari hasil perhitungan

print(x ** y) #	Perpangkatan
print(x // y) #	Pembulatan 		'Pembagian yang dibulatkan dengan bilangan terdekat dari hasil pembagian'
