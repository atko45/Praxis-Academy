# 	Operator 'BITWISE'

h = 5
h &= 3 # Cara membacanya : Bitwise 5 AND 3 sama dengan 1
print(h)

i = 5
i |= 3 # Cara membacanya : Bitwise 5 OR 3 sama dengan 7
print(i)

j = 5
j ^= 3 # Cara membacanya : Bitwise 5 XOR 3 sama dengan 7
print(j)

k = 5
k >>= 3 # Cara membacanya : Bitwise 5 SHIFT RIGHT 3 sama dengan 0
print(k)

l = 5
l <<= 3 # Cara membacanya : Bitwise 5 SHIFT LEFT 3 sama dengan 40
print(l)