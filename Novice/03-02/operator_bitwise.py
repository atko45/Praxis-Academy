	#	ASSIGNMENT Operators

a = 5
a += 3	# Cara membacanya : a = a(5) + 3
print(a)

b = 5
b -= 3
print(b)

c = 5
c *= 3
print(c)

d = 5
d /= 3
print(d)

e = 5
e %= 3
print(e)

f = 5
f //= 3
print(f)

g = 5
g **= 3
print(g)
